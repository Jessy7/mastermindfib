    private void btnOkMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnOkMousePressed

        Boolean ready = true;

        for (int i = 0; i < columns; i++) {
            if (codePegs[currentRow][i].getBackground() == defaultCodePegsColor)
                ready = false;
        }

        if (ready) 
        {
            this.setGuess();
            ((PlayGameUseCaseController)pg).generateKeyPegs();
            this.updateKeyPegs();
            ((PlayGameUseCaseController)pg).closeRound();
            play();
        }

    }//GEN-LAST:event_btnOkMousePressed

